class Vehicle{
    int no_of_seats;
    int no_of_wheels;

    void display(){
        System.out.print("No of seats: "+no_of_seats+ "\nNo of wheels: "+no_of_wheels);
    }

}
public class Task3 {
    public static void main(String[] args) {
        Vehicle car = new Vehicle();
        car.no_of_seats = 4;
        car.no_of_wheels = 4;
        Vehicle Motorcycle = new Vehicle();
        Motorcycle.no_of_seats = 2;
        Motorcycle.no_of_wheels = 2;
        System.out.println("Description of car");
        car.display();
        System.out.println("\nDescription of Motorcycle");
        Motorcycle.display();

    }
}
